package com.ronglian.ch.model;

public class ReportScientificResearch {
	private String id; 
	private String year;  
	private String disease_name; 
	private Integer scientific_report_num;  
	private String doctor_name; 
	private Integer finish;  
	private Integer notfinish; 
	private Integer sum1;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getDisease_name() {
		return disease_name;
	}
	public void setDisease_name(String disease_name) {
		this.disease_name = disease_name;
	}
	public Integer getScientific_report_num() {
		return scientific_report_num;
	}
	public void setScientific_report_num(Integer scientific_report_num) {
		this.scientific_report_num = scientific_report_num;
	}
	public String getDoctor_name() {
		return doctor_name;
	}
	public void setDoctor_name(String doctor_name) {
		this.doctor_name = doctor_name;
	}
	public Integer getFinish() {
		return finish;
	}
	public void setFinish(Integer finish) {
		this.finish = finish;
	}
	public Integer getNotfinish() {
		return notfinish;
	}
	public void setNotfinish(Integer notfinish) {
		this.notfinish = notfinish;
	}
	public Integer getSum1() {
		return sum1;
	}
	public void setSum1(Integer sum1) {
		this.sum1 = sum1;
	}
	
	
}
